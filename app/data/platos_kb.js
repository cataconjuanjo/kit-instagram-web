export function buscarPlatoKb(consulta) {
  const texto = consulta
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')

  return platosKb.find(plato => plato.match.some(alias => texto.includes(alias)))
}
